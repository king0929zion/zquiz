const Map<String, String> subjectSkillPrompts = {
  'chinese': _chineseSkillPrompt,
  'chemistry': _chemistrySkillPrompt,
  'physics': _physicsSkillPrompt,
};

String skillPromptForSubject(String subject) {
  if (subject == 'all') {
    return subjectSkillPrompts.values.join('\n\n');
  }
  return subjectSkillPrompts[subject] ?? '';
}

